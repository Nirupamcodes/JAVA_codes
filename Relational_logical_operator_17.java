public class Relational_logical_operator_17 {

    public static void main(String[]args) {

        boolean a = true;
        boolean b = true;

        if (a && b){

            System.out.println("y");
        }
        else {
            System.out.println("n");
        }

        if (a || b){

            System.out.println("y");
        }
        else {
            System.out.println("n");
        }

        if (a != b){ 

            System.out.println("y");
        }
        else {
            System.out.println("n");
        }



        
    }
    
}
